public class Main {

    public static void main(String[] args) {

        System.out.println("Tipos promitivos:");

        byte b1 = -50; // Inicializando direto a variável.
        System.out.println("Meu número byte: " + b1);

        short s1 = 18550;
        System.out.println("Meu número short: " + s1);

        int i1 = 650;
        System.out.println("Meu número int: " + i1);

        long l1 = 22652330L;
        System.out.println("Meu número long: " + l1);

        float f1 = 2541.32f;
        System.out.println("Meu número float: " + f1);

        double d1 = 2541.33;
        System.out.println("Meu número double: " + d1);

        char c1 = 'S';
        System.out.println("Meu char: " + c1);

        String word = "Java não é difícil";
        System.out.println("Minha String: " + word);

        boolean bo1 = true; // or false
        System.out.println("Meu boolean: " + bo1);
    }

}